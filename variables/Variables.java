

public class Variables {
    public static void main(String[] args) {
        //Variable
        String name = "Edgar";
        System.out.println(name);
        name = "Rojas";
        System.out.println(name);

        
        int age = 23;
        System.out.println(age);

        //Variable aue se tipea solo
        var email = "edgar@rojas.com";

        System.out.println(email);


        //Constantes

        final String EMAIL = "edgar@gmail.com";
        System.out.println(EMAIL);
    }
    
}
